                                  Logika fuzzy pertama di kenalkan oleh Prof. Lotfi A. Zadeh pada tahun 1965. Logika fuzzy merupakan suatu metode pengambilan keputusan berbasis aturan yang digunakan untuk memecahkan keabu-abuan masalah pada sistem yang sulit dimodelkan atau memiliki ambiguitas. Dasar logika fuzzy adalah teori himpunan fuzzy
                                  <br><center><img src="img/fuzzy1.jpg" width="60%" align="center" height=" auto"></center><br>
                                  <p  align="center"><b>Gambar 2.1</b> Diagram Blok ‘Logika Fuzzy sebagai Black box’</p>
                                  <br>

                                  Pada Gambar 2.1 logika fuzzy dapat dianggap sebagai kotak hitam yang berhuungan antara ruang input menuju ruang output (Kusuma Dewi, 2003). Kotak hitam yang dimaksudkan adalah metode yang dapat digunakan untuk mengolah data input menjadi output dalam bentuk informasi yang baik.

                                  <br><br>Adapun beberapa alasan mengapa digunakannya logika fuzzy adalah:

                                  <br>1.  Konsep logika fuzzy mudah dimengerti.

                                  <br>2.  Penggunaan logika fuzzy yang fleksibel.

                                  <br>3.  Logika fuzzy mampu memodelkan fungsi-fungsi nonlinear yang sangat kompleks.


                                  <br>4.  Tidak perlu adanya proses pelatihan untuk memodelkan pengtahuan yang dimiliki oleh pakar.

                                 <br> 5.  Logika  fuzzy  didasari  pada  bahasa  sehari-hari  sehingga  mudah dimengerti.

                                  <br><br>Himpunan fuzzy disebut himpunan tegas (crisp), nilai keanggotaan suatu item x dalam

                                  suatu himpunan A yang dituliskan dengan [x], dimana memiliki dua buah kemungkinan nilai yaitu:

                                 <br> 1.  Satu (1), yang memiliki arti bahwa suatu item menjadi anggota dalam suatu himpunan tertentu.
                                   

                                  <br>2.  Nol (0), yang memiliki arti bahwa suatu item tidak menjadi anggota dalam suatu himpunan tertentu.

                                  <br><br>Himpunan fuzzy memiliki dua atribut yaitu:

                                 <br> 1.  Lingustik, merupakan penamaan grub yang mewakili suatu keadaan atau kondisi tertentu dengan menggunakan bahasa alami/sehari-hari. Contohnya : PENDEK, SEDANG, TINGGI

                                  <br>2.  Numeris, merupakan sutau nilai angka yang menunjukkan ukuran dari suatu variabel. Contohnya : 140, 160, 180

