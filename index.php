                                  <br>Representasi Linear adalah pemetaan input ke derajat keanggotannya digambarkan sebagai suatu garis lurus. Pada representasi linear terdapat 2 kemungkinan, yaitu:
                                  <br><b>Kenaikan</b> himpunan dimulai pada keanggotaan nol (0) bergerak ke arah derajat keanggotaan lebih tinggi.nilai domain yang memiliki derajat kanan menuju nilai domain yang memiliki
